              <h3 class="title">Paying Less Tax</h3>
              <p>Did you know that you pay less tax when you make money with money than by making money by
                providing services?</p>
              <p>There are ways to pay lesser tax. Always find a way to pay lesser tax if possible.</p>
              <ul>
                <li>Do not get a home loan because the money and the interest you pay will not be taxed</li>
                <li>Do not get a loan in general for the tax benefits.</li>
              </ul>
              <p>If you do the calculations, you will know that it is better to pay taxes than to take a loan. If
                you take a home loan for $100,000 and you pay $12,000 every year for the next 15 years, you will
                not be paying tax for the $12,000 but you will be paying around $180,000 in the span of 15 years
                to the banks.</p>
              <p>Payment to bank = $12,000 x 15 = $180,000</p>
              <p>You pay $80,000 to the bank towards interest alone.</p>
              <p>if you pay taxes on the $12,000 for the next 15 years, assuming that the tax will be 30%, you
                will have.</p>
              <p>$8,400 (take home for $12,000) * 15 = $126,000</p>
              <p>$180,000 – $126,000 = $54,000</p>
              <p>You pay $54,000 to the government in taxes.</p>
              <p>There are ways to prevent a certain amount of money from being taxed and help it grow at a steady
                rate.</p>
              <p>Examples are ROTH IRA / IRA in the US, Public Provident Fund (PPF) / Employee Provident Fund
                (EPF) in India.</p>
              <p>I am sure every country has its own name but the point remains the same. You either pay the
                government or invest in the stock market before you obtain your pay. Hence that sum will not be
                accounted for taxes.</p>
              <p>Employee Provident Fund and Public Provident Fund in India do not tax upon withdrawal.</p>
              <p>ROTH IRA in the US demands the tax to be paid while entering the fund but has a tax free growth
                once the money is in the ROTH IRA.</p>
              <p>IRA in the US demands a tax to be paid upon withdrawal.</p>
              <blockquote>
                <p class="blockquote blockquote-success">“It is always better to pay the taxes before growth
                  than after growth”</p>
              </blockquote>
              <p>You will be paying more otherwise.</p>
