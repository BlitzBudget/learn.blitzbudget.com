                It was the best financial move for him and life was happy for a few years until Kevin’s parents
                started seeking an alliance.</p>
              <p>We still practice arranged marriages in India where parents would seek alliance and ask their
                children to marry them. It was back in 2003 when the “US invasion of Iraq” began. Bombshells
                would hit the country of Kuwait. So people were afraid to marry him as it would mean that the
                girl goes to Kuwait and risks her life.</p>
              <p>Having searched for an alliance for years unsuccessfully, the family members of Kevin arranged
                for a new job in Dubai. A much safer country when compared to Kuwait back in the days of Iraq
                war. Kevin finally got married and was successfully working in Dubai. Even though the salary
                wasn’t much, his wife would also aid him financially by working in Dubai. It was a happy family.
                They couldn’t save a lot but they were getting along.</p>
              <p>I honestly do not know what hit them. After eight years in Dubai as a physiotherapist, Kevin
                moved back to India. I could only imagine that such a move had a more emotional explanation than
                a rational explanation. That was when I was doing my undergraduate and even I knew at that time
                that it was a wrong decision! I even told them but what’s the use of telling them after they
                have moved to India.</p>
              <p>Now, he had borrowed ₹1,000,000 to rent a space and make it a hospital to treat patients. Don’t
                ask me what happened to the savings which Kevin had while he was working in Dubai.</p>
              <p>I should only imagine that they had spent it all and came back to India empty-handed. Probably
                lived an extravagant life or earned too little. I would never know and Kevin was not exactly
                transparent about his financial situation with anyone.</p>
              <p>So, Kevin’s new hospital is being totally new and only starting to attract new customers. It had
                been one year since Kevin opened a new hospital in India. As you know, any hospital requires
                reputation and Kevin was by all means building his own reputation in India. He was also very
                stubborn about building his from scratch. One could only wonder why!</p>
              <p>He also had an opportunity to buy a 10-year-old hospital with regular customers at a much
                affordable price which he instantly denied.</p>
              <p>It all turns out to be a mistake only when you give up. It is never a wrong decision until you
                give up! Kevin gave up on Dubai. It was the wrong decision. Kevin gave up on building his
                reputation in India and migrated to Canada a couple of years later.</p>
