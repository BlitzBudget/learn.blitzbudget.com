                had a 8-year-old kid at that time. He imagined a better place in Canada. Till today, He is
                working as a part-time physiotherapist and could not find a more permanent job, During his stay
                in Canada, Kevin had another child. They are now a happy family of four.</p>
              <p>For their migration, they had borrowed another ₹500,000 more. Now, they owe a grand total of
                ₹1,500,000. How could Kevin repay the amount in full when he is struggling financially?</p>
              <p>Borrowing from relatives has its own perks. You do not have to pay interest on the money borrowed
                but when you fail to repay the money promptly, you risk losing the relationship. I guess that’s
                what happened with Kevin. He lost the people close to him because he wanted a financial shelter
                under their roof.</p>
              <p>He is now slowly starting to repay the borrowed amounts but the damage has already been done.
                Everyone looks to him as a person who had missed the opportunity to be someone better.</p>
