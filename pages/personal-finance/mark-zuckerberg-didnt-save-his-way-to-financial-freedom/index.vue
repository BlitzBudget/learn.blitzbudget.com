                  Lifetime.</li>
                <li>Jeff Bezos who learned his key business skills from his grandfather (who was a very
                  successful scientist, businessman, and landowner)</li>
                <li>Warren Buffet’s father (Howard Homan Buffett) was a successful American businessman,
                  investor, and politician.</li>
              </ul>
