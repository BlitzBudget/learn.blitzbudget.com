                him money to start the “Facebook”.</p>
              <p>In short, their parent did all the hard work to push their son to the top, just so that their son
                can do the remaining effort.</p>
              <p>Edward Zuckerberg was a dentist and successfully did his part of the bidding, saved enough money
                to become wealthy and make sure that their children have an advantage when they start living
                their life.</p>
              <p>You can find this to be true in most of the other successful billionaires.</p>
