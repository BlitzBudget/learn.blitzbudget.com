                credit score of 620.</p>
              <p>Should you really save up 30% of your income before buying a home?</p>
              <p><strong>No! Definitely not.</strong></p>
              <p>Let’s assume that you are buying a home worth $200,000. The minimum required down payment would
                then be $6000 which means that you will be taking a mortgage of $194,000.</p>
              <p>That is a considerable mortgage which attracts an average compound interest of 4.6%.</p>
              <p>Even if you choose to pay the mortgage in 360 installments which is a 30 year period, you would
                be paying around $1358 on a monthly basis which is substantial for an average person earning
                about $30,000 a year.</p>
              <p>You would have paid around <strong>$468,841.66</strong> for a 30 year period.</p>
              <p>How does it make sense to get a 3% down payment option then?</p>
              <p>Let's look at the bigger picture.</p>
              <ul>
                <li>From the lenders perspective, 3% down payment option is riskier.</li>
                <li>From a government's perspective, they are too desperate to induce artificial growth.</li>
              </ul>
              <p>Any rational person will know that if a person cannot possibly save 30% of the house value, he
                cannot pay off the house.</p>
              <p>For a government to provide such a radical scheme, they must be really desperate for growth.</p>
              <p>After introducing a 3% option for buying a home, anybody who cannot afford to save 30% of the
                total value of the home (which is the case with 37% of the families in the US) will start to go
                for the 3% option.</p>
              <p>There is a sudden spike in the housing market. All the people who couldn’t previously afford to
                buy a home are now eligible to buy one with not much commitment at all. They are only paying 3%
                of the total value of a home.</p>
              <p>That’s not much of a ownership, the bank practically owns the home. The only belief is that the
                debtor will repay the mortgage on a monthly basis.</p>
              <p>The sudden spike in the housing price will induce urgency and more and more people will start
                buying a home. So, if there is a 3% option, the best way is to get a new home with a 3% down
                payment.</p>
              <p>What next? All the bad debtors will start defaulting on the payment. If enough people default on
                the mortgage then it will be a successful recreation of the 2008 housing bubble.</p>
