              <h3 class="title">The Best Index Fund</h3>
              <p>Selecting an index fund is equivalent to selecting a simplistic straightforward fund with minimum
                charges.</p>
              <p>All index funds were not created equal. Some are to be avoided at all costs.</p>
              <p>Index funds that carry a sales load or commission, for example. It does not make sense to pay a
                front-end load or a back-end load in an index fund. The sales load is a straightforward cost
                which is to be avoided given the fact that the index funds with no load are available.</p>
              <p>Front-end sales load is a fee to enter the fund, usually a percentage of invested capital. For
                example, if you are investing $5000 in an index fund with a sales load of 5%, you would have to
                pay $5250 to enter the fund and the $250 would be the front-end sales load.</p>
              <p>Similarly, the back-end sales load is a fee that is charged to exit the fund usually a percentage
                of invested capital. For example, an investment of $5000 has grown to $10,000 and upon
                withdrawal, the index fund is charging you a back-end sales load of 5%. You would have to pay
                $500 upon withdrawal of $10,000.</p>
              <p>Front-end sales are the least evil of both loads as you will be paying a straight fee before your
                money has grown.</p>
              <p>The backend sales load is the worst of all evil as you would be paying a percentage of the total
                money being withdrawn. Usually, the invested money would have grown many folds in an index fund.
                You would be paying a lot of money compared to the front end load.</p>
              <p>The best fund would be a no-load fund with minimal hidden fees like marketing fees, brokerage
                fees and an expense ratio less than 0.50%.</p>
              <p>All returns calculated by the fund managing companies are pre-tax returns. Although the
                industries ignore taxes, they take a major chunk of the market returns. as high as 20% in some
                of the cases. So it makes sense to hold an index fund in a non-taxable account.</p>
              <p>In the US, the best option would be to buy an index fund with the above-mentioned criteria
                through an IRA or better yet through ROTH IRA.</p>
