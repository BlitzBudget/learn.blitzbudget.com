                trustworthiness of her brother.
              </p>
              <p>
                What happens if my friend buys a thali for his own mother whom he loves. His mother will continue to
                trust her brother as before always as if nothing has changed while her brother could continue to take
                full financial advantage of her.
              </p>
              <p>
                My friend's sister is a chartered account and she knows what has been happening as well. She is just too
                afraid to speak up. My friend is in Spain. What could he possibly do from 8000 km away which his mother
                and sister cannot possibly do?
              </p>
              <p>
                My friend still believes that he will get the money back from his uncle. So, I had to dig in deeper. No
                matter how ugly the truth is. My friend was willing to know it after all “Trust has to be earned”.
              </p>
              <p>
                So, I started asking him questions about his uncle. How much does he earn every month? How is he earning
                them? What are his assets?
              </p>
              <p>
                Thinking deeply, my friend replied “He might be earning about ₹10,000 every month. Definitely not more
                than that! He is currently running a business which acts as a middleman between the customer and the
                government to obtain any certificate or document from the government ASAP”.
              </p>
              <p>
                To shed some light upon the truth, I continued “In other words, It would take your uncle approximately
                25 years to accumulate the sum of ₹3,000,000 even if he saved all of the income generated from his
                business?”. I never mentioned about the inflation and the reduction of purchase power of the ₹3,000,000,
                25 years later but he got the picture.
              </p>
              <p>
                He replied “Hmm….”. Now he was starting to lose hope in ever getting the money back. The idea of his
                buying a home for his family was starting to fade away. He started to worry about his financial
                situation for the first time probably in his lifetime. His dreams shattered! He started to get ahold of
                himself. I could see the pain in his eyes. It deeply saddened me to have done this to him. It was at
                times like these that I remember “It is sometimes better to let a man live in his dreams, not revealing
                the truth if you know he is happy there‘.
              </p>
              <p>
                It was too late for me now. I had to end what I had started. So I continued “including the 30 sovereigns
                of gold which your uncle has borrowed the sum is ₹3,700,000 which would take your uncle about 30 years
                to repay and just to return the thali, your uncle needs to save all of his income for 10 months
                straight”.
              </p>
              <p>
                It would cost around ₹100,000 to buy a new thali for my friend's mother but his mother was eagerly
                waiting for his brother to provide her with the means to buy a new thali and my friend was also
                believing the same till then. I shattered it again!
              </p>
              <p>
                His uncle was known for his lavish spending. Later, my friend also told me that he is now starting to
                suspect that his uncle had used a part of the money which his mom gave for safekeeping to renew the
                house where he lived which cost about ₹1,100,000. “It was a couple of years ago when my uncle renewed
                his home. He never muttered a word about the overdraft on the joint fixed deposit then. They deposited
                it 3 years ago and he clearly had access to those funds” my friend continued.
              </p>
              <p>
                The total spending accounted for about ₹1,600,000. But it still didn’t account for the remaining amount.
                His uncle also mentioned that he has spent a total of ₹1,800,000 and the remaining ₹200,000 was loaned
                to one of his friends and he was expecting it to be returned in a few months.
              </p>
              <p>
                I asked my friend to ask his uncle to return all the remaining money, whatever he has left of the fixed
                deposit. He is yet to get a penny from his uncle.
              </p>
              <p>
                The real cost of not taking control of your financial life! My friend is now starting to take control of
                his finances. Although the future may seem bleak, he trusts that he could make a difference. He is
                puffing up his emergency fund now and has a clear road ahead.
              </p>
