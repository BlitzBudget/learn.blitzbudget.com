                the amount with the mortgage”
              </p>
              <p>
                I have to say at this point that his uncle was of much help to my friends family when they needed it
                most. He had sponsored four years of his high school education and his sister's education. He also
                sponsored an operation for his mother and honestly my friends family had no money to pay for the
                operation back then. All these would financially account for about ₹500,000.
              </p>
              <p>
                I convinced my friend that his uncle had utilized all the money for sure and he had no intention of
                repaying the money back.
              </p>
