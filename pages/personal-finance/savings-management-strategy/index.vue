                and I promptly informed my friend not to as it would prove to be a dire mistake.
              </p>
              <p>
                He also informed me that his uncle is investing in bitcoins because he thinks it is the hot new
                investment. During this six hours conversation which we had recently, he said: “the bitcoin investment
