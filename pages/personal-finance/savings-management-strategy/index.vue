              <p>
                Short-term investment vessel includes your car as well. Car is a depreciating asset but you could put
                the money in a fixed deposit till you accumulate an adequate amount in order to buy a car.  The last
                thing which you should be doing is to buy a car on a loan.
                <br />
                Save your allocated budget as a recurring deposit if you are in India or in a fixed deposit. Keep it
                safe and risk-free.
              </p>
              <p>
                Short-term investment vessels are generally very risky.  For example, Bitcoin investment in the wake of
                2017. Bitcoins’ stocks soared from $1000 by more than 1300% to $14,500 by the end of the year.
