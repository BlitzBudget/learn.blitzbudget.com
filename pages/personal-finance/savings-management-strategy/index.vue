                “Wow” was the word which came to my mind. He continued “He also took all of my mother’s gold. All of our
                30 sovereigns of it including my mother’s thali and it was five years ago. He still did not return an
                ounce of gold which he borrowed ”.
              </p>
              <p>
                Thali is the equivalent of a wedding ring in European countries. The validity of the marriage is stored
                in the form of a symbol “Thali” and the emotions around it are so strong.
              </p>
              <p>
                For a married woman to donate it for the sake of his brother is a true act of love & trust.
              </p>
              <p>
                That was all of their families wealth. Everything! They live in a rented apartment in the town and his
