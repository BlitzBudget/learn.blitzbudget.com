                ₹1,200,000 and that he intended to repay the amount in a few months.
              </p>
              <p>
                My friends' mother was fully aware that the fixed deposit was expiring by May 2018, after which his
                mother promptly requested for the money back. He somehow convinced his mother of his intentions to repay
                the money in the coming few months and in fact, he had withdrawn ₹1,800,000.
              </p>
              <p>
                When they asked for the remaining amount to be repaid, he had no intention to repay the remaining
                ₹1,200,000 and is intentionally postponing the repayment. Once his mother had forced an answer from his
